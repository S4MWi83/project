
public class DesignProject {
	
	public DesignProject(String name, PicturePane pane) {
		this.name = name;
		this.pane = pane;
	}
	
	public String getName() {
		return name;
	}
	public PicturePane getPane() {
		return pane;
	}
	
	private String name;
	private PicturePane pane;
	
}
